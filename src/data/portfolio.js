export function getProjectById(id) {
  return projectCases.find((project) => project.id === id);
}

export function getProjectPreview(project, maxLength = 160) {
  if (project.shortPreview) return project.shortPreview;
  const first = project.fullDescription?.[0] ?? "";
  if (first.length <= maxLength) return first;
  return `${first.slice(0, maxLength).trimEnd()}…`;
}

